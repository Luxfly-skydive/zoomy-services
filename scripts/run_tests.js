function isFallback(r) {
  const fn = norm(r);
  return fn.includes('not sure') || fn.includes('rephrase') || fn.includes('je ne suis pas') ||
         fn.includes('no estoy') || fn.includes('could you rephrase') || fn.includes('didnt quite') ||
         fn.includes('n ai pas') || fn.includes('pouvez vous reformuler') || fn.includes('hmm') ||
         fn.includes('reformularlo') || fn.includes('please rephrase') || fn.includes('entendi bien') ||
         fn.includes('entendido') || fn.includes("didn't get") || fn.includes("catch that");
}

function test(label, input, checkFn) {
  try {
    const result = getResponse(input);
    const ok = checkFn(result);
    if (ok) { passed++; }
    else {
      failed++;
      errors.push(`FAIL [${label}]\n  input: "${input}"\n  got:   "${result.slice(0,100)}"`);
    }
  } catch(e) {
    failed++;
    errors.push(`ERR  [${label}]\n  input: "${input}"\n  err:   ${e.message}`);
  }
}

function notFallback(label, input) { test(label, input, r => !isFallback(r)); }
